
public class WhileLoop {

 public static void main(String[] args) {
        int i=1;
        System.out.println("Odd \t Even");
        while(i<=10)
        {
             if(i%2 == 1)
             {
                System.out.print(i+"\t");
             }
             else
             {
            	 System.out.println(i);
             }
             i=i+1;
    }

 }

}